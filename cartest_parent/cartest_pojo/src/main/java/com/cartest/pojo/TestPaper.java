package com.cartest.pojo;

public class TestPaper {
    private int paper_id;
    private String question_id_seq;
    private String key_seq;

    public int getPaper_id() {
        return paper_id;
    }

    public void setPaper_id(int paper_id) {
        this.paper_id = paper_id;
    }

    public String getQuestion_id_seq() {
        return question_id_seq;
    }

    public void setQuestion_id_seq(String question_id_seq) {
        this.question_id_seq = question_id_seq;
    }

    public String getKey_seq() {
        return key_seq;
    }

    public void setKey_seq(String key_seq) {
        this.key_seq = key_seq;
    }
}
